	<p>
	<strong>Marcelo Martinez</strong> é designer gráfico e ilustrador, nascido no Rio de Janeiro. 
	Em 2011, recebeu um dos Prêmios Literários Fundação Biblioteca Nacional pelo 
	projeto gráfico do <i>Livro-Jogo das Copas Globo Esporte</i>, editado pela Casa da Palavra. 
	Em 2010, o portfólio do Laboratório Secreto foi destacado na revista chinesa <i>New Graphic Design</i>, 
	inaugurando uma série especial de reportagens sobre o design brasileiro. 
	Em 2009 participou, a convite do Netherlands Photo Museum, da mostra de artes gráficas Brazil Contemporary, 
	em Roterdã, Holanda. Em 2008, teve seu trabalho incluído no livro <i>Latin American Graphic Design</i>, 
	editado pela Taschen Books, apresentando 200 nomes relevantes do design gráfico latino-americano. 
	Martinez e sua equipe tiveram ainda projetos exibidos – e por vezes premiados – em mostras de design, 
	ilustração e animação em mais de 15 países das Américas Latina e do Norte, Ásia, Europa e África; 
	e registrados em mais de uma dúzia de publicações internacionais. Teve trabalhos selecionados nas 
	últimas cinco edições da Bienal Brasileira de Design Gráfico, desde 2000.
	Faz parte do conselho-diretor da SIB – Sociedade dos Ilustradores do Brasil, é membro da ADG Brasil – 
	Associação dos Designers Gráficos, e professor de direção de arte na Escola de Design da UniverCidade – RJ. 
	Ministra workshops e palestras em diversas cidades brasileiras.
	</p>
