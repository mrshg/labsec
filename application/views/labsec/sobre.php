<strong style="font-weight:bold;">Marcelo Martinez </strong>é designer gráfico e ilustrador, nascido no Rio de Janeiro. <br/>Em 2011, recebeu um dos Prêmios Literários Fundação Biblioteca Nacional pelo projeto gráfico do <i>Livro-Jogo das Copas Globo Esporte,</i> editado pela Casa da Palavra. Em 2010, o portfólio do Laboratório Secreto foi publicado com destaque na revista chinesa <i>New Graphic Design</i>,
 inaugurando uma série especial de reportagens sobre o design 
brasileiro. Em 2009 participou, a convite do Netherlands Photo Museum, 
da mostra de artes gráficas "Brazil Contemporary", em Roterdã, Holanda. 
Em 2008, teve seu trabalho incluído no livro <i>Latin American Graphic Design</i>,
 editado pela Taschen Books, apresentando 200 nomes relevantes do design
 gráfico latino-americano. Martinez e sua equipe tiveram ainda projetos 
exibidos – e por vezes premiados – em mostras de design, ilustração e 
animação em mais de 15 países das Américas Latina e do Norte, Ásia, 
Europa e África; e registrados em mais de uma dúzia de publicações 
internacionais. Teve trabalhos selecionados para as últimas cinco 
edições da Bienal Brasileira de Design Gráfico.<br/>

<br/>Faz parte do conselho-diretor da SIB – Sociedade dos Ilustradores do
 Brasil e é membro da ADG Brasil – Associação dos Designers Gráficos. 
Ministra workshops e palestras em diversas cidades brasileiras. <br/><br/>Criado
 em 2004, o Laboratório Secreto é um estúdio de design gráfico e 
ilustração, especializado em projetos para os mercados editorial, 
cultural e publicitário.<br/>
